

integer_type = 10
float_type = 10.5
complex_type = 3 + 4j

string_type = "Hello, World!"


list_type = [1, 2, 3, 4, 5]
tuple_type = (1, 2, 3, 4, 5)
range_type = range(1, 6)


set_type = {1, 2, 3, 4, 5}
frozenset_type = frozenset({1, 2, 3, 4, 5})


dict_type = {"key1": "value1", "key2": "value2"}


boolean_type = True


bytes_type = b"Hello, World!"
bytearray_type = bytearray(5)
memoryview_type = memoryview(bytes_type)


none_type = None


